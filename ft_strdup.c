#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	size_t	s_size;
	size_t	i;

	s_size = ft_strlen(s) + 1;
	dest = malloc(s_size * sizeof(char));
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = 0;
	return (dest);
}
